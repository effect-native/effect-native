#!/usr/bin/env node
import { existsSync } from 'node:fs'
import { lstat, readdir } from 'node:fs/promises'
import path from 'node:path'

const args = process.argv.slice(2)
if (args.includes('--help') || args.includes('-h')) {
  console.log('Usage: bun scripts/bundle-stats.mjs --base-dir <path>')
  console.log('Compares build/dist bundle bytes between the current checkout and a base checkout.')
  process.exit(0)
}

const baseDir = parseArg(args)
const rootDir = process.cwd()
const packagesDir = path.join(rootDir, 'packages')

const packageEntries = await readdir(packagesDir, { withFileTypes: true })

const packageStats = await Promise.all(
  packageEntries
    .filter((entry) => entry.isDirectory())
    .map(async (entry) => {
      const packageDir = path.join(packagesDir, entry.name)
      const manifest = path.join(packageDir, 'package.json')
      if (!existsSync(manifest)) {
        return null
      }

      const currentSize = await getDirectorySize(packageDir, ['build', 'dist'])
      let baseSize = 0

      if (baseDir) {
        const basePackageDir = path.join(rootDir, baseDir, 'packages', entry.name)
        if (existsSync(basePackageDir)) {
          baseSize = await getDirectorySize(basePackageDir, ['build', 'dist'])
        }
      }

      const delta = currentSize - baseSize
      return {
        name: entry.name,
        current: currentSize,
        base: baseSize,
        delta
      }
    })
)

const rows = packageStats.filter(Boolean)

rows.sort((a, b) => Math.abs(b.delta) - Math.abs(a.delta))

const totalCurrent = rows.reduce((sum, row) => sum + row.current, 0)
const totalBase = rows.reduce((sum, row) => sum + row.base, 0)
const totalDelta = totalCurrent - totalBase

const outputLines = [
  '## Bundle Size Analysis',
  '',
  '| Package | Current | Base | Delta |',
  '| --- | ---: | ---: | ---: |',
  ...rows.map((row) => `| ${row.name} | ${formatBytes(row.current)} | ${formatBytes(row.base)} | ${formatSignedBytes(row.delta)} |`),
  `| **Total** | ${formatBytes(totalCurrent)} | ${formatBytes(totalBase)} | ${formatSignedBytes(totalDelta)} |`,
  '',
  `Generated by \`bun run bundle-stats${baseDir ? ` -- --base-dir ${baseDir}` : ''}\``,
  '',
  '<!-- This comment was auto-generated by GitHub Actions to display bundle size statistics -->'
]

await Bun.write('stats.txt', `${outputLines.join('\n')}\n`)

function formatBytes(value) {
  if (value === 0) {
    return '0 B'
  }

  const units = ['B', 'KB', 'MB', 'GB']
  let size = value
  let unitIndex = 0

  while (size >= 1024 && unitIndex < units.length - 1) {
    size /= 1024
    unitIndex += 1
  }

  const rounded = size >= 10 ? size.toFixed(1) : size.toFixed(2)
  return `${rounded} ${units[unitIndex]}`
}

function formatSignedBytes(value) {
  const sign = value >= 0 ? '+' : '-'
  return `${sign}${formatBytes(Math.abs(value))}`
}

async function getDirectorySize(packageDir, folderNames) {
  let totalSize = 0

  for (const folder of folderNames) {
    const target = path.join(packageDir, folder)
    if (!existsSync(target)) {
      continue
    }
    totalSize += await size(target)
  }

  return totalSize

  async function size(directory) {
    let total = 0
    const entries = await readdir(directory, { withFileTypes: true })

    for (const entry of entries) {
      const childPath = path.join(directory, entry.name)
      const stats = await lstat(childPath)
      if (stats.isDirectory()) {
        total += await size(childPath)
      } else if (stats.isFile()) {
        total += stats.size
      }
    }

    return total
  }
}

function parseArg(argv) {
  const index = argv.indexOf('--base-dir')
  if (index !== -1 && argv[index + 1]) {
    return argv[index + 1]
  }

  return null
}
